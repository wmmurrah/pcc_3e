# Locations I would like to visit.
locations = ["key west","bermuda", "azores", "greece", "capri"]

print(locations)

# Locations temporarily sorted alphabetically
print(sorted(locations))

print(locations)

# Reverse temporary sorting

print(sorted(locations, reverse=True))
print(locations)

# Change order permenantly
locations.reverse()

print(locations)

locations.reverse()

print(locations)

locations.sort()
print(locations)

locations.sort(reverse=True)
print(locations)